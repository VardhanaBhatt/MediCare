    private void populateCombo() {
        cbOrgType.removeAllItems();
        for (Organization.OrgType type : Organization.OrgType.values()) {
            if (type.getValue().equals(Organization.OrgType.SeniorCare.getValue()) || type.getValue().equals(Organization.OrgType.Medical.getValue())) {
                cbOrgType.addItem(type);
            }
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrg.getModel();
        model.setRowCount(0);

        for (Organization organization : organizationDirectory.getOrganizationList()) {
                Object[] row = new Object[1];
                row[0] = organization;

                model.addRow(row);
        }
    }

